// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: two numbers
//O: one number
//C/E: Only return a number if one is smaller, otherwise return 0
function min(a, b) {
  if (a < b){
    return a;
  } else if (a > b){
    return b;
  } else {
    return 0;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: one number
//O: a boolean value 
//C: recursive, should work with positive AND negative whole numbers
//E: Zero, 1

function isEven(num) {
  if (num < 0){
    return isEven(-num);
  } else if (num == 0){
    return true;
  } else if (num == 1){
    return false;
  } else {
    return isEven(num - 2);
  }
}

